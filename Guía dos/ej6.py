
ops = int(
    input('Seleccione operacion: 1. suma 2. resta 3. división 4. multiplicación '))

if (ops > 0 < 5):
    numb = int(input('Ingresa un número: '))
    numb2 = int(input('Ingresa un número: '))
    if (ops == 1):
        print(numb + numb2)
    elif (ops == 2):
        print(numb - numb2)
    elif (ops == 3):
        print(numb / numb2)
    elif (ops == 4):
        print(numb * numb2)
